// Fallback — generate from a generic range if country not mapped
const FALLBACK_RANGES = [[185,0],[91,0],[45,0],[103,0],[78,0],[62,0]];

/**
 * Generate a random IP address that falls within known ranges for a country.
 * @param {string} countryCode - ISO 2-letter country code (e.g. "GB", "DE")
 * @returns {string} IP address string
 */
function generateCountryIP(countryCode) {
  const code = (countryCode || '').toUpperCase();
  const ranges = COUNTRY_IP_RANGES[code] || FALLBACK_RANGES;

  // Pick a random range
  const range = ranges[Math.floor(Math.random() * ranges.length)];
  const octet1 = range[0];
  const octet2 = range[1] + Math.floor(Math.random() * 8); // slight variation
  const octet3 = Math.floor(Math.random() * 256);
  const octet4 = 1 + Math.floor(Math.random() * 254); // avoid .0 and .255

  return `${octet1}.${octet2}.${octet3}.${octet4}`;
}

/**
 * Get list of supported country codes
 */
function getSupportedCountries() {
  return Object.keys(COUNTRY_IP_RANGES);
}

module.exports = { generateCountryIP, getSupportedCountries, COUNTRY_IP_RANGES };
