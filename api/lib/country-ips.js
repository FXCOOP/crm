/**
 * Generate a random IP from a country's residential IP ranges.
 * @param {string} countryCode - ISO 2-letter code (e.g. "PT", "GB")
 * @returns {string} IP address
 */
function generateCountryIP(countryCode) {
  const code = (countryCode || '').toUpperCase();
  const ranges = COUNTRY_IP_RANGES[code];

  if (!ranges || ranges.length === 0) {
    // If unknown country, return null so caller knows
    return null;
  }

  // Pick a random range
  const range = ranges[Math.floor(Math.random() * ranges.length)];
  const [start, end] = range;

  // Generate random IP within the range
  const span = end - start;
  const randomOffset = Math.floor(Math.random() * span);
  const ip = start + randomOffset;

  // Avoid .0 and .255 in last octet
  let lastOctet = ip & 255;
  if (lastOctet === 0) lastOctet = 1;
  if (lastOctet === 255) lastOctet = 254;

  return intToIp((ip & 0xFFFFFF00) | lastOctet);
}

function getSupportedCountries() {
  return Object.keys(COUNTRY_IP_RANGES);
}

module.exports = { generateCountryIP, getSupportedCountries, COUNTRY_IP_RANGES };
