/**
 * Country-specific residential IP ranges.
 * Each entry: [startIP, endIP] as 32-bit integers.
 * Ranges sourced from major residential ISPs per country.
 * NO datacenter/cloud IPs (no AWS, GCP, Azure, OVH).
 */

// Convert IP string to 32-bit integer
function ipToInt(ip) {
  const parts = ip.split('.').map(Number);
  return ((parts[0] << 24) | (parts[1] << 16) | (parts[2] << 8) | parts[3]) >>> 0;
}

// Convert 32-bit integer to IP string
function intToIp(num) {
  return [
    (num >>> 24) & 255,
    (num >>> 16) & 255,
    (num >>> 8) & 255,
    num & 255,
  ].join('.');
}

// Build ranges from readable format
function r(start, end) {
  return [ipToInt(start), ipToInt(end)];
}

